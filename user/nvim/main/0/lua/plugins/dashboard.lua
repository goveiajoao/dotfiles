			local logo = logos[logoID]
			local logoHeight = #vim.split(logo, "\n")
			local height = vim.api.nvim_win_get_height(0)
			local centralizer = math.floor(height / 4)
			local initCentralizer = centralizer - math.floor(logoHeight / 2)
			local postCentralizer = centralizer * 2 - (logoHeight + initCentralizer)
